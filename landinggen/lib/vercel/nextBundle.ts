const FIREBASE_LEADS_TS = `import { addDoc, collection, serverTimestamp } from "firebase/firestore";
import { getDb } from "@/lib/firebase/client";

export type LeadInput = {
  name: string;
  email: string;
  company?: string;
  message?: string;
};

export async function createLead(input: LeadInput, clientId?: string) {
  const db = getDb();
  if (!db) throw new Error("Firebase not configured");

  const resolvedClientId = (clientId || "default").trim() || "default";

  const docRef = await addDoc(collection(db, "leads", resolvedClientId, "entries"), {
    ...input,
    clientId: resolvedClientId,
    createdAt: serverTimestamp(),
    userAgent: typeof navigator !== "undefined" ? navigator.userAgent : null,
    referrer: typeof document !== "undefined" ? document.referrer : null,
    url: typeof location !== "undefined" ? location.href : null,
  });

  return { id: docRef.id };
}
`;

