function sanitizeProductionPageTsx(source: string) {
  // Ensure the deployed app is a single-page landing.
  // We accept whatever the model outputs, but enforce default export if missing.
  if (/export\s+default\s+function\s+Page\s*\(/.test(source)) return source;
  if (/export\s+default\s+function\s+(\w+)\s*\(/.test(source)) return source;
  if (/export\s+default\s+/.test(source)) return source;
  return `export default function Page() {\n  return (\n    <div className="min-h-screen bg-black text-white p-8">Missing <code>export default function Page()</code></div>\n  );\n}\n`;
}

